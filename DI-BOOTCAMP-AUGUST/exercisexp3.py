def run_once(func):
    def wrapper(*args, **kwargs):
        if not hasattr(wrapper, 'has_run'):
            wrapper.has_run = True
            wrapper.result = func(*args, **kwargs)
            return wrapper.result
        return None
    return wrapper

@run_once
def add_decorated(a,b):
    return a + b

