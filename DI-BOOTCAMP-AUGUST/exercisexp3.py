print("\n17. Fonction once:")
def once(func):
    has_run = False
    result = None
    def wrapper(*args, **kwargs):
        nonlocal has_run, result
        if not has_run:
            has_run = True
            result = func(*args, **kwargs)
            return result
        return None
    return wrapper

def add(a,b):
    return a + b

one_addition = once(add)

print(f"one_addition(2,2) # {one_addition(2,2)}")
print(f"one_addition(2,2) # {one_addition(2,2)}")
print(f"one_addition(12,200) # {one_addition(12,200)}")

