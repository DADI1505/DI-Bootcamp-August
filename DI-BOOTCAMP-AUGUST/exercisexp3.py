def list_manipulation(lst, command, location, value=None):
    if command == "remove":
        if location == "end":
            if lst: return lst.pop()
        elif location == "beginning":
            if lst: return lst.pop(0)
    elif command == "add":
        if location == "beginning":
            lst.insert(0, value)
            return lst
        elif location == "end":
            lst.append(value)
            return lst
    return None

list1 = [1,2,3]
